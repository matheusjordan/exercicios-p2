package exercicio3;


public class Inteiro {
	
	//Questão 1
	public static boolean isPar(int numero) {
		return numero % 2 == 0;
	}
	
	//Questão 1
	public static boolean isImpar(int numero) {
		return numero % 2 == 1;
	}
	
	//Questão 2
	public static int valorAbs(int numero) {
		return numero < 0 ? numero * -1 : numero;
	}
}
