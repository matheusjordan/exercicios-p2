package exercicio6;

public class Calculadora {
	public static double some(double n1, double n2) {
		return n1 + n2;
	}
	
	public static double subtraia(double n1, double n2) {
		return n1 - n2;
	}
	
	public static double multiplique(double n1, double n2) {
		return n1 * n2;
	}
	
	public static double divida(double n1, double n2) {
		return n1 / n2;
	}
}
